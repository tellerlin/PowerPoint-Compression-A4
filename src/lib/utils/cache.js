export class CacheManager {
  constructor(expiryTime = 1800000) {
    this.cache = new Map();
    this.expiryTime = expiryTime;
  }

  set(key, value) {
    this.cache.set(key, {
      value,
      timestamp: Date.now()
    });
  }

  get(key) {
    const item = this.cache.get(key);
    if (!item) return null;
    
    if (Date.now() - item.timestamp > this.expiryTime) {
      this.cache.delete(key);
      return null;
    }
    
    return item.value;
  }

  clear() {
    this.cache.clear();
  }
}

export const imageCache = new CacheManager();